# Type Conversion
#   implicit type conversion.
#   Type conversion is the automatic conversion of one data type into another by Python.
#   a = 10      # int
#   b = 2.5     # float
#   c = a + b
#   print(c)
#   print(type(c))
#   output:12.5
#   <class 'float'>

# Type Casting:
#    explicit type conversion
#   Type casting is the manual conversion of one data type into another by the programmer using built-in functions.
#   Uses functions like int(), float(), str(),bool(),dict(),list(),tuple()
#   x = "50"
#   print(int(x) + 10)
#   output:60