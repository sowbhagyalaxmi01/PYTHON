# Rules  for identifers (variable names)
#      A variable name must start with a letter or the underscore character
#     A variable name cannot start with a number
#     A variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ )
#     Variable names are case-sensitive (age, Age and AGE are  different variables)
#     A variable name cannot be any of the Python keywords.
#     eg:(myvar ,my_var, my_var, myVar, MYVAR ,myvar2)

#  variables names:
#    Camel case:each word,except first,start with a capital letter(myVariableName="sow")
#    pascal case:each word start with capital letter (MyVariableName="sow")
#    snake case:each word is separated by an underscore character(my_var_name)


