#Print numbers from 20 to 40.
for i in range(20,41):
    print(i)

#Print every 3rd number from 1 to 30
for i in range(1,31,3):
    print(i)

#Print every 5th number from 10 to 100
for i in range(10,101,5):
    print(i)


#Print numbers from 100 to 50 with a gap of 5.
for i in range(100,50,-5):
    print(i)


#Print numbers from 0 to 100 with a gap of 10.
for i in range(0,101,10):
    print(i)

#Print numbers from 50 to 0.
for i in range(50,-1,-1):
    print(i)

#Print numbers from -10 to 10.
for i in range(-10,11):
    print(i)

 #Print numbers from 10 to -10.
for i in range(10,-11,-1):
    print(i)

